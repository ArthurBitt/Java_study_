package fundamento;

public class Fundamentos_13_Operadores_Relacionais {
    public static void main(String[] args) {

        // OBS. 97 EM UNICODE EQUIVALE A a
        int a = 97;
        int c = 'a';
        int b = 96;

        System.out.println(a == c);
        System.out.println(a > b);
        System.out.println(b < a);
        System.out.println(b <= a);
        System.out.println(b >= a);
        System.out.println(a != b);



    }
}
